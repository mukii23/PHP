You can downgrade the PHP XAMPP version by using this code:


Download and extract the file xampp-win32-5.6.36-0-VC11.zip(eg. portable xampp version)

Delete php folder and apache folder present in C:\xampp (currently installed)

Copy php folder and apache folder from extracted file and paste it to C:\xampp

Add " C: " before \xampp\ to php ini file present in php folder.

Start your apache and MySQL and check php version. It will show php 5.6.36
